    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int index = 0;
        String retener = cmbProductos.getSelectedItem().toString();
        index = cuentas.buscarNumero(articulos, retener);
        cantidad[index] = cantidad[index] + Double.parseDouble(txtCantidad.getText());
        total[index] = cantidad[index] * Double.parseDouble(txtPrecio.getText());
        JOptionPane.showMessageDialog(null, cantidad[index]);
        JOptionPane.showMessageDialog(null, retener);
        JOptionPane.showMessageDialog(null, total[index]);

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalActionPerformed
        String[] ordenado = cuentas.mayor(articulos, cantidad);
        double importe = cuentas.sumar(total);
        double[] porcentaje = cuentas.porcentajeProducto(total);
        double[] subtotal = cuentas.subtotalProducto(cantidad, precios);
        StringBuilder contexto = new StringBuilder();
        String titulo = "Reporte final";
        contexto.append("<html><body>");
        contexto.append("<h1><u>REPORTE DE VENTAS</u></h1>");
        contexto.append("<hr><h2>Productos del m√°s al menos vendido </h2>");
        contexto.append("<ol>");
        for (int i = 0; i < ordenado.length; i++) {
            contexto.append("<li>").append(ordenado[i]).append("</li>");
        }
        contexto.append("</ol>");
        contexto.append("<hr><h2>Importe total de ventas </h2>");
        contexto.append(importe).append(" $");
        contexto.append("<hr><h2>Porcentaje de venta por producto </h2>");
        contexto.append("<ol>");
        for (int i = 0; i < porcentaje.length; i++) {
            contexto.append("<li>").append(String.format("%.2f", porcentaje[i])).append(" " + ordenado[i]).append("</li>");
        }
        contexto.append("</ol>");
        contexto.append("<hr><h2>Subtotales de venta por producto </h2>");
        contexto.append("<ol>");
        for (int i = 0; i < subtotal.length; i++) {
            contexto.append("<li>").append(String.format("%.2f", subtotal[i])).append(" " + ordenado[i]).append("</li>");
        }
        contexto.append("</ol>");
        contexto.append("<hr><h1><u>FIN DEL REPORTE</u></h1>");
        contexto.append("</body></html>");
        JOptionPane.showMessageDialog(null, contexto, titulo, JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnFinalActionPerformed

